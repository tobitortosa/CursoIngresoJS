/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.*/
function mostrar() {
  let numAcendente;

  numAcendente = 1; // Variable de Control

  while (numAcendente < 11) {
    alert(numAcendente);
    numAcendente++;
  }
}
