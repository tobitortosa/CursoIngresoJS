/*
al presionar el botón mostrar 10 repeticiones 
con números DESCENDENTES, desde el 10 al 1.*/
function mostrar() {
  let numDecente;

  numDecente = 10; // Variable de Control

  while (numDecente > 0) {
    alert(numDecente);
    numDecente--;
  }
}
